<template>
    <header>
        <h1>
            <img src="../assets/logo.png" alt="">
        </h1>
        <button class="button" @click="alterTheme">{{ btnText }}</button>
    </header>
</template>

<script lang="ts">
import {defineComponent} from 'vue'

export default defineComponent({
    name: "BarraLateral",
    emits: ['onAlterTheme'],
    data () {
        return {
            darkMode: false
        }
    },
    computed: {
        btnText () {
            if(this.darkMode) {
                return 'Desativar Dark Mode'
            }

            return 'Ativar Dark Mode'
        }
    },
    methods: {
        alterTheme () {
            this.darkMode = !this.darkMode
            this.$emit('onAlterTheme', this.darkMode);
            console.log('ttestando')
        }
    }
})
</script>


