<template>
  <section>
    <!-- <strong>00:00:00</strong> -->
    <strong>{{ elapsedTime }}</strong>
  </section>
</template>

<script lang="ts">
import { defineComponent } from 'vue'
export default defineComponent({
    name: 'Cronometro',
    props: {
        timeInSeconds: {
            type: Number,
            default: 0
        }
    },
    computed: {
        elapsedTime(): string {
            return new Date(this.timeInSeconds * 1000).toISOString().substr(11, 8);
        }
    },
})
</script>

<style>
    strong {
        color: var(--text-color);
    }
</style>